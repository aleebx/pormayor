<h5>2. Réplicas, falsificaciones, copias no autorizadas, copia de imagen y/o descripción</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB"><p>Los artículos que no permitimos publicar son:</p>
	<ul class="browser-default">
		<li>La venta de copias, falsificaciones o réplicas no autorizadas que violen los derechos de Propiedad Intelectual.</li>
		<li>La venta de copias no autorizadas de software, videojuegos, discos, películas, programas de televisión, fotografías, etc.</li>
		<li>El uso de logos o marcas en productos que no fueron realizados oficialmente por el poseedor de los derechos de Propiedad Intelectual.</li>
		<li>La venta de documentación que permita certificar la originalidad de un producto.</li>
		<li>Libros comercializados en formatos no autorizados por los titulares de los derechos de autor.</li>
		<li>El uso, sin previa autorización, de imágenes o descripciones de publicaciones de otros Usuarios del “Sitio”.
</li>
	</ul>
</div>




