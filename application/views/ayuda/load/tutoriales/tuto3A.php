<h5>¿Cómo convierto un producto en favorito?</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB">
	<ul>
		<li>1. Selecciona el producto que deseas convertir en favorito </li>
		<br>
		<li>2. Toca en <img class="positionRelative" src="//pormayor.pe/img/fav.svg" type="image/svg+xml"> que se encuentra bajo los botones sociales de facebook</li>
		<br>
	</ul>
	<p><b>Si deseas modificar tus favoritos </b></p>
	<ul>
		<li>1. Toca en <i class="positionRelative material-icons pormayorAccent-text">account_circle</i> y selecciona <b>PERFIL DE USUARIO</b>.</li>
		<br>
		<li>1b. También puedes tocar en <img alt="" class="positionRelative" src="//pormayor.pe/img/menuIcon.svg" width="30"> y seleccionar <b><i class="positionRelative material-icons pormayorAccent-text">account_box</i> Perfil de Usuario</b></li>
		<br>
		<li>2. Toca en <b><i class="positionRelative material-icons pormayorAccent-text">favorite</i> Favoritos</b> en el menú lateral.<span class="orange-text"><b>Si estás utilizando tu télefono celular debes primero abrir el menú tocando en <img alt="" class="positionRelative" src="//pormayor.pe/img/menuIcon.svg" width="30"></b></span></li>
		<br>
		<li>3. Puedes revisar el producto tocando el botón <b>VER PRODUCTO</b> o puedes eliminarlo de favoritos tocando el botón <b>ELIMINAR</b></li>
	</ul>
</div>