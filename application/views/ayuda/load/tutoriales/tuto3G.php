<h5>Para finalizar la compra de un pedido cotizado </h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB">
	<ul>
		<li>1. Toca en <i class="positionRelative material-icons pormayorAccent-text">account_circle</i> y selecciona <b>PERFIL DE USUARIO</b>.</li>
		<br>
		<li><span class="green-text"><b>1b. Otra Opción.</b></span>  También puedes tocar en <img alt="" class="positionRelative" src="//pormayor.pe/img/menuIcon.svg" width="30"> y seleccionar <b><i class="positionRelative material-icons pormayorAccent-text">account_box</i> Perfil de Usuario</b></li>
		<br>
		<li>2. Toca en <b><i class="positionRelative material-icons pormayorAccent-text">playlist_add_check</i> Pedidos</b> en el menú lateral.<span class="orange-text"><b>Si estas utilizando tu télefono celular debes primero abrir el menú tocando en <img alt="" class="positionRelative" src="//pormayor.pe/img/menuIcon.svg" width="30"></b></span></li>
		<br>
		<li>3. Selecciona el pedido <b>con cotización</b> con el cual decidas procesar la compra</li>
		<br>
		<li>4. Toca el botón <b>VER COTIZACIÓN</b></li>
		<br>
		<li>5. Realiza las operaciones que desees con la cotización y una vez te encuentres conforme toca el botón <b>PROCESAR COMPRA</b></li>
		<br>
		<li>6. Elige entre la opción de <b>COMPRA SEGURA</b> o <b>ACUERDO CON TIENDA</b> y toca la opción seleccionada.</li>
		<br>
		<li>7. <span class="green-text"><b>Si seleccionaste la opción COMPRA SEGURA</b></span>. Escribe la <b>dirección de envio</b>, selecciona el banco en el cual deseas pagar la compra y finalmente toca <b>FINALIZAR COMPRA</b></li>
		<br>
		<li>7b.  <span class="orange-text"><b>Si seleccionaste la opción ACUERDO CON TIENDA</b></span>. Toca en <b>CONTINUAR</b> para responder afirmativamente a la pregunta y finalizar la compra</li>
	</ul>
</div>