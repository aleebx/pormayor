<h5>Para calificar compras realizadas</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB">
	<ul>
		<li>1. Toca en <i class="positionRelative material-icons pormayorAccent-text">account_circle</i> y selecciona <b>PERFIL DE USUARIO</b>.</li>
		<br>
		<li><span class="green-text"><b>1b. Otra Opción.</b></span> También puedes tocar en <img alt="" class="positionRelative" src="//pormayor.pe/img/menuIcon.svg" width="30"> y seleccionar <b><i class="positionRelative material-icons pormayorAccent-text">account_box</i> Perfil de Usuario</b></li>
		<br>
		<li>2. Toca en <b><i class="positionRelative material-icons pormayorAccent-text">shopping_cart</i> Mis compras</b> en el menu lateral.<span class="orange-text"><b>Si estas utilizando tu télefono celular debes primero abrir el menu tocando en <img alt="" class="positionRelative" src="//pormayor.pe/img/menuIcon.svg" width="30"></b></span></li>
		<br>
		<li>3. Selecciona la compra que deseas calificar</li>
		<br>
		<li>4. Toca en <b><i class="positionRelative material-icons pormayorAccent-text">thumbs_up_down</i> Calificar compra</b></li>
		<br>
		<li>5. Califica la <b>compra</b> con la opción que desees y luego toca el botón <b>CONTINUAR</b> </li>
		<br>
		<li>6. Califica la <b>tienda</b> con la opción que desees, <b>describe tu experiencia</b> al comprar con la tienda y finalmente toca el botón <b>FINALIZAR MI CALIFICACIÓN</b> </li>
		<br>
		<li>7. Visualiza tu calificación y toca el botón <b>ENTENDIDO</b> para finalizar el proceso </li>
	</ul>
</div>