<h5>¿Dónde contesto las cotizaciones que me solicitan?</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB">
	<ul>
		<li>1. Entra en tu <b><i class="positionRelative material-icons pormayorAccent-text">settings</i> Panel de tienda</b></li>
		<br>
		<li>2. Toca en <b><i class="positionRelative material-icons pormayorAccent-text">work</i> Gestión</b> en el menú lateral y a continuación toca en <b><i class="positionRelative material-icons pormayorAccent-text">playlist_add_check</i> Pedidos</b>.
			<span class="orange-text"><b>Si estas utilizando tu télefono celular debes primero abrir el menú tocando en <img alt="" class="positionRelative" src="//pormayor.pe/img/menuIcon.svg" width="30"></b></span></li>
		<br>
		<li>3. Selecciona el pedido que deseas contestar</li>
		<br>
		<li>4. Coloca los precios a cada uno de los productos solicitados, y luego toca en el botón <b>ENVIAR</b>. <span class="orange-text"><b>Si estas contestando una cotización nuevamente debes presionar el boton REENVIAR</b></span></li>
		<br>
		<li>4b. Si tocas en el boton <b>DESCARGAR</b> podrás descargar toda la cotización en un archivo <i>PDF</i></li>
		<br>
	</ul>
</div>