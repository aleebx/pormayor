<h5>Para agregar más locales de tu tienda</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB">
	<ul>
		<li>1. Entra en tu <b><i class="positionRelative material-icons pormayorAccent-text">settings</i> Panel de tienda</b></li>
		<br>
		<li>2. Toca en <b><i class="positionRelative material-icons pormayorAccent-text">store</i> Tienda</b> en el menu lateral.
			<span class="orange-text"><b>Si estas utilizando tu télefono celular debes primero abrir el menu tocando en <img alt="" class="positionRelative" src="//pormayor.pe/img/menuIcon.svg" width="30"></b></span></li>
		<br>
		<li>3. Selecciona <b><i class="positionRelative material-icons pormayorAccent-text">business</i> Agrega la dirección de tus locales</b> y luego toca en el botón <br><b><i class="positionRelative material-icons pormayorAccent-text">add</i> AGREGAR</b></li>
		<br>
		<li>4. Escribe el número de telefono del nuevo local </li>
		<br>
		<li>5. Mueve el <b>icono del mapa</b> hasta seleccionar la dirección deseada (verifica que el nombre sea correcto en el campo de <i>Dirección</i>) y luego toca en el boton <b> AGREGAR</b></li>
		<br>
		<li><b><span class="orange-text">5b. También puedes editar los locales ya agregados tocando en el botón </span><i class="positionRelative material-icons pormayorAccent-text">edit</i>,<span class="orange-text"> o eliminarlos haciendo lo propio con el botón </span><i class="positionRelative material-icons pormayorAccent-text">delete</i></b></li>
	</ul>
</div>