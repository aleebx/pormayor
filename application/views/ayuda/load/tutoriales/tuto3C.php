<h5>¿Cómo agrego/elimino productos al pedido actual?</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB">
	<p><b>Para agregar productos</b></p>
	<ul>
		<li>1. Selecciona el producto que deseas agregar al pedido actual</li>
		<br>
		<li>2. Escribe una <b>cantidad de unidades</b> validas con respecto al minimo de venta del producto. <span class="green-text"><b>Puedes escribir las cantidades en todas las variaciones que desees</b></span></li>
		<br>
		<li>3. Toca el botón <b>AGREGAR</b> que se encuentra antes del bloque de comentarios. Visualiza una notificación que te indica si el Producto fue <span class="green-text"><b>agregado al pedido actual</b></span> o <span class="orange-text"><b>si ocurrió algún problema</b></span></li>
		<br>
	</ul>
	<p><b>Para eliminar productos</b></p>
	<ul>
		<li>1. Toca en el ícono <img src="//pormayor.pe/img/pedidoIcon.svg" style="filter: brightness(0%)" class="positionRelative" height="40"> para visualizar tu pedido actual.<span class="orange-text"><b>Si utilizas tu télefono celular este icono se encentra en la barra superior</b></span></li>
		<br>
		<li>2. Toca en <i class="positionRelative material-icons pormayorAccent-text">clear</i> correspondiente al producto que deseas <b>eliminar de tu pedido actual</b> y contesta afirmativamente a la pregunta que se mostrará a continuación.</li>
		<br>
		<li>2b. Si tocas en <i class="positionRelative material-icons pormayorAccent-text">delete_sweep</i> <b>eliminarás todos los productos</b> de tu pedido actual.</li>
	</ul>
</div>