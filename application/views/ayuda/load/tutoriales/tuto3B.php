<h5>¿Qué significa el minimo de venta de un producto?</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB">
	<p>Para poder <b>cotizar un producto</b> debes cumplir una unica condición, y esa es la de escribir una cantidad igual o mayor a la impuesta por la tienda vendedora del producto.</p>
	<br>
	<p>Este minimo de venta no es más que la cantidad de <b>unidades</b> minimas en las que el producto puede ser cotizado, asi que si por ejemplo quieres cotizar <b>Polos manga larga</b> y estos tienen un minimo de venta de <i>5 unidades</i>. Debes cotizar 5 o más unidades para continuar con tu pedido, de lo contrario no podrás agregar el producto a tu pedido actual.</p>
</div>