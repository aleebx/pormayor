<h5>¿Por qué puedo colocar un número de cuenta como dato personal?</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB">
	<p>Si decides ingresar tu número de cuenta en la plataforma, ésta será enviada a tus compradores al momento de finalizar una compra con el método de <b>ACUERDO CON TIENDA</b>. Por lo tanto este dato <span class="orange-text"><b>no es obligatorio para la creación de tienda</b></span></p>
