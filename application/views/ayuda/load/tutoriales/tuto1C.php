<h5>Para crear una tienda en pormayor.pe</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB">
	<ul>
		<li>1. Sigue <a class="pormayorUnico-text" href="http://localhost/betapormayor/tienda/crear">este enlace</a></li>
		<br>
		<li>2. Selecciona si tu RUC es <b>Natural</b> o <b>Jurídico</b></li>
		<br>
		<li>3. Escribe un DNI válido y deja que nuestro sistema confirme tus datos</li>
		<br>
		<li>4. Escribe un correo electronico válido (aqui llegarán las cotizaciones que te pidan, preguntas de compradores, entre otros)</li>
		<br>
		<li>5. Crea una contraseña y toca <b>SIGUIENTE</b></li>
		<br>
		<li>6. <span class="orange-text"><b>Si tu RUC es Natural puedes omitir este paso.</b></span> Escribe un numero de RUC válido y deja que nuestro sistema confirme tus datos</li>
		<br>
		<li>7. Escribe un nombre para tu tienda y toca <b>CREAR TIENDA</b> para finalizar el proceso</li>
	</ul>
</div>