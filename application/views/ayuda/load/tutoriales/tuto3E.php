<h5>¿Cómo cotizo mi pedido actual?</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB">
	<ul>
		<li>1. Toca en  <img src="//pormayor.pe/img/pedidoIcon.svg" style="filter: brightness(0%)" class="positionRelative" height="40"> para visualizar tu pedido actual.<span class="orange-text"><b>Si utilizas tu télefono celular este icono se encentra en la barra superior</b></span></li>
		<br>
		<li>2. Toca en el botón <b>COTIZAR</b></li>
		<br>
		<li>3. Verifica los productos que se van a cotizar y a continuación escribe tu <b>teléfono</b> en caso de no estar agregado. Opcionalmente también puedes escribir un comentario para enviar con el pedido</li>
		<br>
		<li>4. Toca el botón <b>ENVIAR PEDIDO</b> y a continuación toca en <b>ENVIAR</b> para finalizar la operación.</li>
	</ul>
</div>