<h5>3. Comisión de Venta por el “Servicio de CompraSegura”</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB"><p>El Usuario Comprador tiene la opción de pagar al Usuario Vendedor en dos formas principales:</p>
	<ul class="browser-default">
		<li>Pago directo al Usuario vendedor. Realizada a través de una transferencia bancaria, u otra acordada entre las partes.</li>
		<li>Pago mediante "Servicio de CompraSegura".</li>
	</ul>

<p>En cuanto a el Pago mediante "Servicio de CompraSegura". Esta modalidad permite que el Usuario Comprador realice un pago mediante un código autogenerado y único por cada compra en la entidad financiera que tenga convenio <b>Pormayor.pe</b>, Este pago hecho por el  Usuario Comprador se tiene bajo custodia en la “Cuenta Recaudadora” de Pormayor,pe S.A. hasta que el Usuario Comprador haya dado su conformidad de lo recibido en la plataforma, teniendo un plazo máximo de 72 horas contabilizados desde el momento que se genera el pago total por el pedido solicitado en la entidad financiera que haya elegido y se tenga convenio la empresa.
</p>

<p>Pasadas las 120 horas, y  si no hay ninguna calificación a la compra hecha, se entiende como su total conformidad con lo recibido.
La calificación del Usuario Comprador autoriza la transferencia de lo pagado hacia el Usuario Vendedor, salvo que el Usuario Comprador interponga un reclamo, debiéndose retener la transferencia hasta que esté solucionado la controversia según los Términos y Condiciones del “Servicio de CompraSegura” y documentos anexos.  La Comisión de Venta por el “Servicio de CompraSegura” que en adelante se llamará  la “Comisión por Venta”  es un monto fijo establecido por venta concretada bajo esta modalidad.</p>
Dicha comisión tiene las siguientes características:
	<ul class="browser-default">
		<li>Se cobra al Vendedor la suma de S/ 5.00 (cinco soles) por operaciòn</li>
		<li>Es una tarifa plana, sin importar el monto de la operación realizada</li>
		<li>Es descontada del “monto total del pedido” depositado en la “Cuenta Recaudadora” por el comprador para el pedido.</li>
		<li>La “Comisión por Venta” se cobrará en Soles.</li>
	</ul>
<p>Los costos bancarios generados por la operaciones de venta, especificamente la comisión interplaza (costo de envio de dinero de una provincia a otra) seran asumidos por <b>PorMayor.pe</b> S.A. Adicionalmente, <b>Pormayor.pe</b> descontará al Usuario Vendedor y comprador el ITF (impuesto a las transacciones financieras) según corresponda.</p></div>








