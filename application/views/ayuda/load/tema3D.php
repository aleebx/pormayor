		<li>Miras para armas de fuego, así como también cualquier tipo de miras infrarrojas o detección de calor.</li>
		<li>Balas y municiones para armas de fuego.</li>
		<li>Productos o dispositivos cuyo fin sea el armado de explosivos.</li>
		<li>Máquinas para recarga y fabricación de municiones.</li>
		<li>Herramientas o equipos que permitan modificar armas.</li>
		<li>Planos que enseñen a modificar armas de fuego.</li>
