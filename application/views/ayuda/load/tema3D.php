<h5>4. Armas, municiones y material explosivo.</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB"><p>Está completamente prohibido publicar y/o solicitar:</p>
	<ul class="browser-default">
		<li>Armas de fuego de alto o bajo calibre: armas deportivas, de colección, reliquias o antiguas (disparen o no).</li>
		<li>Parte de armas de fuego, accesorios o elementos relacionados con el funcionamiento del arma: cañones, correderas, gatillos, silenciadores, etc.
