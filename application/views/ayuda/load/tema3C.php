	<p>La responsabilidad tanto de la legalidad y legitimidad de los artículos asumen los vendedores que los ofrecen. Quedando <b>Pormayor.pe</b> exento de responsabilidad alguna por la existencia en el sitio de productos que infrinjan las políticas o cualquier ley o resolución judicial vigente.</p>
