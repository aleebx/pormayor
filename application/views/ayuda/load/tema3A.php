<h5>1. Publicaciones que violen la propiedad intelectual</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB"><p><b>Pormayor.pe</b> actuando en pro de la Protección de la Propiedad Intelectual tiene el objetivo de impedir que sean listados u ofrecidos a través de <b>Pormayor.pe</b> artículos que violen algún Derecho de Propiedad Intelectual, sea derecho de autor, de patentes, de marcas, modelos y/o diseños industriales u otros derechos de terceros. Los titulares de derechos o cualquier Usuario podrán identificar y solicitar la remoción de aquellos artículos que a su criterio infrinjan o violen lo establecido en los Términos y Condiciones de Uso de <b>Pormayor.pe</b>.</p>

<p>Todo Usuario que crea que un artículo está en contra de las políticas del “Sitio” tiene derecho a reportarlo. Teniendo <b>Pormayor.pe</b> el derecho de dar de baja aquellas publicaciones que no respeten nuestras Políticas de Publicación o cualquier Ley vigente, esto llegando a la inhabilitación de la “Cuenta”.</p>

<p>El artículo prohibido de publicar es:
Publicaciones que violen las leyes sobre derecho de autor, patentes, marcas y modelos y diseños industriales.</p></div>



