<h5>3. Incumplimiento del compromiso de compra</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB"><p>Toda oferta en <b>Pormayor.pe</b> es de naturaleza firme, es decir, en cada operación de compra y venta de un producto publicado en <b>Pormayor.pe</b> el Usuario adquiere un compromiso ineludible.</p>

<p>En caso de que un Usuario reincide en 2 oportunidades de no cumplir con dichos compromisos, <b>Pormayor.pe</b> se reserva el derecho de suspender sus operaciones en el “Sitio”.</p></div>

<h5>4. Incumplimiento del compromiso de venta</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB"><p>El Usuario adquiere un compromiso por cada publicación de sus productos en el “Sitio”: Al concretarse una venta el Usuario deberá hacer la entrega del producto en las condiciones que haya detallado en su publicación.</p>

<p>Y, siendo el caso que de manera reiterada un Usuario incumpla su compromiso de entrega del producto, Pormayor.pe podrá inhabilitar su “Cuenta”</p></div>




