<h5>4. Comisión de Compra por el “Servicio de CompraSegura”</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB"><p>La inscripción de un Usuario Comprador en <b>Pormayor.pe</b> es gratuita. Por cada compra realizada mediante el “Servicio de CompraSegura”, <b>Pormayor.pe</b> adicionará una comisión (en adelante la “Comisión de Compra”) que representa un monto fijo establecido por compra concretada.</p>
Dicha comisión tiene las siguientes características:
	<ul class="browser-default">
		<li>Se cobra al Usuario Comprador la suma S/ 3.00 (tres soles) por la compra en una “Tienda Virtual”.</li>
		<li>Es un monto único establecido por cada compra en una tienda realizada a través del “Servicio de CompraSegura”.</li>
		<li>Este monto está adicionado en el valor de la compra a pagar  en cada pedido concretado. </li>
		<li>La “Comisión de Compra” se cobrará en Soles.</li>
		<li>La “Comisión de Compra” será descontada del “monto total del pedido” depositado por el Usuario Comprador en la “Cuenta Recaudadora” de <b>Pormayor.pe</b>.</li>
	</ul>
</div>