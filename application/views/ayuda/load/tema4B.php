<h5>2. Contratación de Tiendas Virtuales</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB"><p>Acceder al “Servicio” de Tiendas Virtuales requiere un pago previo. Sin embargo, se diferencian por Planes-categorías.</p>

Características generales:
	<ul class="browser-default">
		<li>Dependiendo del tipo de Plan que se contrata, el Usuario debe pagar una tarifa plana, de acuerdo a las tablas de costos que figuran en la parte inferior.</li>
		<li>Dentro de la Tienda Virtual el Usuario realizará publicación de productos la cantidad que tenga en su negocio. Se precisa que a la fecha no existe limitación en el número de productos a ingresar en cada tienda virtual.</li>
		<li>Los Planes varían de acuerdo a meses contratados: 3, 6 y 12 meses. Los Usuarios reconocen y aceptan que <b>Pormayor.pe</b> determinará cada uno de dichos niveles en forma inapelable para cada una de las Tiendas, teniendo en consideración los meses contratados.</li>
		<li>Si un Usuario tuviera su cuenta bloqueada (suspendida o inhabilitada) debido a infracciones cometidas sancionadas, la “Cuenta” activada será cancelada.</div></li>
	</ul>







