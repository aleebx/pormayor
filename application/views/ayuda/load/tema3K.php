<h5>11. Publicaciones que violen derechos de propiedad intelectual y con fines distintos a la venta de un producto o servicio.</h5>
<div class="divider"></div>
<div class="fontParrafo enfasisB"><p>Está completamente prohibido publicar y/o solicitar:</p>
	<ul class="browser-default">
		<li>Productos que violen las leyes sobre derecho de autor, patentes, marcas y modelos y diseños industriales.</li>
		<li>Publicaciones que carezcan de seriedad o estén destinadas a la burla.</li>
		<li>Publicaciones que tengan un fin político.</li>
	</ul>
